package com.Borman.websocket;

public class Webstats {

    private int globalTimeSpent;
    private int usersOnline;

    public int getGlobalTimeSpent() {
        return globalTimeSpent;
    }

    public void setGlobalTimeSpent(int globalTimeSpent) {
        this.globalTimeSpent = globalTimeSpent;
    }

    public int getUsersOnline() {
        return usersOnline;
    }

    public void setUsersOnline(int usersOnline) {
        this.usersOnline = usersOnline;
    }
}
